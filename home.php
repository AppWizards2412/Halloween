   <script>
    // JavaScript for slideshow functionality
    const offerSlides = document.querySelectorAll('.offer-slide');
    let currentSlideIndex = 0;

    function showNextSlide() {
        offerSlides[currentSlideIndex].style.display = 'none';
        currentSlideIndex = (currentSlideIndex + 1) % offerSlides.length;
        offerSlides[currentSlideIndex].style.display = 'block';
    }

    // Initially show the first slide
    offerSlides[0].style.display = 'block';

    // Automatically switch to the next slide every 5 seconds
    const slideInterval = setInterval(showNextSlide, 5000);

    // Clear the interval when user interacts with the slideshow
    for (const slide of offerSlides) {
        slide.addEventListener('mouseenter', () => clearInterval(slideInterval));
        slide.addEventListener('mouseleave', () => slideInterval = setInterval(showNextSlide, 5000));
    }

    // JavaScript for triggering the animation
    const aboutSection = document.querySelector('.about-us');
    const aboutDetails = document.querySelector('.about-details');

    function isElementInViewport(element) {
        const rect = element.getBoundingClientRect();
        return (
            rect.top >= 0 &&
            rect.bottom <= (window.innerHeight || document.documentElement.clientHeight)
        );
    }

    function handleScrollAnimation() {
        if (isElementInViewport(aboutSection)) {
            aboutDetails.classList.add('active');
            window.removeEventListener('scroll', handleScrollAnimation);
        }
    }

    // Trigger the animation on scroll
    window.addEventListener('scroll', handleScrollAnimation);
</script>




